<h4>Introduction</h4>
<p>Ce système de gestion en ligne offre aux clubs adhérents de <?php echo VAR_LANG_ASSOCIATION_NAME_ARTICLE; ?> une aide
    pour la gestion administrative de leurs membres et facilite le travail
    de <?php echo VAR_LANG_ASSOCIATION_NAME_ARTICLE; ?> concernant le calcul des cotisations et la gestion des
    abonnements au tchouk<sup>up</sup>.</p>
<p>Au début de chaque saison le décompte pour le calcul des cotisations est effectué. Toutefois, vous pouvez indiquer
    les changements d'effectifs de votre club toute l'année afin que vos nouveaux membres puissent recevoir le
    tchouk<sup>up</sup> et que les membres ayant quitté le club cessent de le recevoir.</p>

<h4>Aide à la promotion régionale du tchoukball</h4>
<p>
    Dans le cadre de sa politique d'information et de promotion du tchoukball, Swiss Tchoukball soutient les clubs qui
    désirent envoyer le tchouk<sup>up</sup> à leurs contacts-clé. Ainsi, <?php echo VAR_LANG_ASSOCIATION_NAME_ARTICLE; ?>
    offre un abonnement par 20 membres actifs. Profitez donc d'abonner gratuitement vos VIPs !
</p>

<h4>Définition des catégories</h4>
<dl>
    <dt>Membre actif</dt>
    <dd>Il s'agit de membres sportivement actifs ayant 21 ans (nés en <?php echo date('Y') - 21; ?> ou avant)</dd>
    <dt>Membre junior</dt>
    <dd>Il s'agit de membres sportivement actifs n'ayant pas encore 21 ans (nés en <?php echo date('Y') - 20; ?> ou
        après)
    </dd>
    <dt>Membre soutien</dt>
    <dd>Il s'agit de membres sportivement non actifs qui reçoivent le tchouk<sup>up</sup></dd>
    <dt>Membre passif</dt>
    <dd>Il s'agit de membres sportivement non actifs ne recevant aucune prestation de la part
        de <?php echo VAR_LANG_ASSOCIATION_NAME_ARTICLE; ?></dd>
    <dt>Membre VIP</dt>
    <dd>Il s'agit de personnes ou d'entreprises que le club aimerait abonner au tchoukup afin qu'elles soient tenues au
        courant des activités tchoukballistiques en Suisse. Par exemple: le concierge de la salle, le responsable des
        sports de la municipalité, etc. (cf. paragraphe 'Aide à la promotion du tchoukball')
    </dd>
</dl>

<h4>Protection des données</h4>
<p>En vertu de la loi sur la protection des données, nous vous rendons attentifs que les membres de votre club doivent
    être informés que leurs données personnelles seront transmises à <?php echo VAR_LANG_ASSOCIATION_NAME_ARTICLE; ?>
    afin d'être entrées dans la base de données centrale servant à la gestion des membres et des abonnements au
    tchoukup.</p>
<p>La base de données centrale est protégée et accessible uniquement aux membres du comité exécutif ainsi qu'aux membres
    du secteur comptabilité. Les responsables de la gestion des membres des clubs adhérents n'ont accès qu'aux
    informations relatives aux membres de leurs clubs. Ces données ne sauront en aucun cas être distribuées ou vendues à
    des tiers.</p>